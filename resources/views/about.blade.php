            ve dinî hizmet veren irşad görevlilerinin temini ve eğitimi gibi çeşitli dinî hizmetlerin yerine getirilmesi için
            bir zorunluluk olmuştur. Bu zorunlulukla, 1995 yılında kurulan “İslam Toplumu Millî Görüş (IGMG)” ismini;
            Nahl Suresi 123. ayeti, Âl-i İmrân Suresi 95. ayeti, Nisâ Suresi 125. ayetlerinden hareketle Kur’ân-ı Kerîm’e
            göre “millet” tanımlaması üzerinden almıştır. Bu tanımlama, Peygamber’e inanan ve bu Peygamber’in
            getirdiği değerler, idealler ve gelenekler etrafında toplanan cemaati niteler. Böylece, teşkilatı tarif eden
            “Millî Görüş ” terkibinin anlamı, görüş ve inanış bakımından kendisini Hz. İbrâhim (a.s.)’ın milletinden
            kabul eden ve Hz. Muhammed (s.a.v.)’in yolundan giden İslami bir topluluğun dünyaya bakışı olarak
            anlaşılmalıdır. Bu yaklaşım çerçevesinde yürüttüğü çalışmalarıyla IGMG her geçen gün hizmet götürdüğü
            bölge, şube ve üye sayısını artırarak, bugün bulunduğu bölgelerde Dünya’nın en büyük sivil toplum
            kuruluşları arasında bir konuma ulaşmıştır.
